function interpolate(template: string, variables?: Variables): string {
  if (!variables) return template;
  return template.replace(/\{(\w+)\}/g, (_, key: string) =>
    variables[key] === undefined ? `{${key}}` : String(variables[key]),
  );
}

export function getInitialLanguage(): Language {
  try {
    const stored = window.localStorage.getItem(LANGUAGE_KEY);
    if (stored === 'es' || stored === 'en') return stored;
  } catch {
    // Ignore unavailable storage.
  }
  return window.navigator.language.toLowerCase().startsWith('en') ? 'en' : 'es';
}

export function translate(
  key: string,
  variables?: Variables,
  language: Language = getInitialLanguage(),
): string {
  const template = messages[language][key] ?? messages.es[key] ?? key;
  return interpolate(template, variables);
}

type I18nContextValue = {
  language: Language;
  locale: string;
  setLanguage: (language: Language) => void;
  t: (key: string, variables?: Variables) => string;
};

const I18nContext = createContext<I18nContextValue | null>(null);

export function I18nProvider({ children }: { children: React.ReactNode }) {
  const [language, setLanguageState] = useState<Language>(getInitialLanguage);

  useEffect(() => {
    document.documentElement.lang = language;
    try {
      window.localStorage.setItem(LANGUAGE_KEY, language);
    } catch {
      // Language remains active in memory.
    }
  }, [language]);

  const value = useMemo<I18nContextValue>(
    () => ({
      language,
      locale: language === 'es' ? 'es-MX' : 'en-US',
      setLanguage: setLanguageState,
      t: (key, variables) => translate(key, variables, language),
    }),
    [language],
  );

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n(): I18nContextValue {
  const context = useContext(I18nContext);
  if (!context) throw new Error('useI18n must be used inside I18nProvider');
  return context;
}

export function LanguageSwitcher() {
  const { language, setLanguage, t } = useI18n();
  return (
    <label className="language-switcher">
      <Globe2 aria-hidden="true" />
      <span>{t('language.label')}</span>
      <select
        value={language}
        onChange={event => setLanguage(event.target.value as Language)}
        aria-label={t('language.label')}
      >
        <option value="es">{t('language.es')}</option>
        <option value="en">{t('language.en')}</option>
      </select>
    </label>
  );
}

export function statusLabel(value: string, t: I18nContextValue['t']): string {
  return t(`status.${value}`) === `status.${value}` ? value : t(`status.${value}`);
}

export function taskLabel(value: string, t: I18nContextValue['t']): string {
  return t(`task.${value}`) === `task.${value}` ? value : t(`task.${value}`);
}

export function parameterLabel(key: string, t: I18nContextValue['t']): string {
  const translated = t(`parameter.${key}`);
  return translated === `parameter.${key}` ? key : translated;
}

export function translateProgressMessage(
  message: unknown,
  t: I18nContextValue['t'],
): string {
  if (typeof message !== 'string' || message.length === 0) {
    return t('experiment.notStarted');
  }
  const gpu = message.match(/^GPU\s+(\d+)\s+reservada$/i);
  if (gpu) return t('progress.gpuReserved', { id: gpu[1] });
  const generation = message.match(/^Generación\s+(\d+)\s+de\s+(\d+)\s+completada$/i);
  if (generation) {
    return t('progress.generationCompleted', {
      current: generation[1],
      total: generation[2],
    });
  }
  const map: Record<string, string> = {
    'Cargando dataset': 'progress.loadingData',
    'Dataset no encontrado': 'progress.datasetNotFound',
    'Archivo del dataset no encontrado': 'progress.datasetFileNotFound',
    'Preparando datos y estimador M5GP': 'progress.preparing',
    'Inicializando la población y evaluación inicial': 'progress.initializing',
    'Evaluación inicial completada': 'progress.initialCompleted',
    'Evaluando el mejor modelo con los datos de prueba': 'progress.evaluating',
    'Guardando modelo, métricas y predicciones': 'progress.saving',
    'Experimento finalizado correctamente': 'progress.completed',
    'El experimento terminó con error': 'progress.failed',
    'Ejecución cancelada por el usuario': 'progress.cancelled',
  };
  return map[message] ? t(map[message]) : message;
}
